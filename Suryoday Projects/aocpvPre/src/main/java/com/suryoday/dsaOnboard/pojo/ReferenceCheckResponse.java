package com.suryoday.dsaOnboard.pojo;

public class ReferenceCheckResponse {
	private String id;
	private String name;
	private String bank;
	private String designation;
	private String mobile;
	private String emailId;
	private String remarkFromReference;
	private String serviceProvidedTofinancial;
	private String feedbackRating;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBank() {
		return bank;
	}
	public void setBank(String bank) {
		this.bank = bank;
	}
	public String getDesignation() {
		return designation;
	}
	public void setDesignation(String designation) {
		this.designation = designation;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public String getRemarkFromReference() {
		return remarkFromReference;
	}
	public void setRemarkFromReference(String remarkFromReference) {
		this.remarkFromReference = remarkFromReference;
	}
	public String getServiceProvidedTofinancial() {
		return serviceProvidedTofinancial;
	}
	public void setServiceProvidedTofinancial(String serviceProvidedTofinancial) {
		this.serviceProvidedTofinancial = serviceProvidedTofinancial;
	}
	public String getFeedbackRating() {
		return feedbackRating;
	}
	public void setFeedbackRating(String feedbackRating) {
		this.feedbackRating = feedbackRating;
	}
	
}
