package com.suryoday.twowheeler.pojo;

public class GuardianDetails {
	private String guardianRelationship;
	private String guardianAge;
	private String guardianName;
	private String guardianDob;
	private String guardianAddress;

	public String getGuardianRelationship() {
		return guardianRelationship;
	}

	public void setGuardianRelationship(String guardianRelationship) {
		this.guardianRelationship = guardianRelationship;
	}

	public String getGuardianAge() {
		return guardianAge;
	}

	public void setGuardianAge(String guardianAge) {
		this.guardianAge = guardianAge;
	}

	public String getGuardianName() {
		return guardianName;
	}

	public void setGuardianName(String guardianName) {
		this.guardianName = guardianName;
	}

	public String getGuardianDob() {
		return guardianDob;
	}

	public void setGuardianDob(String guardianDob) {
		this.guardianDob = guardianDob;
	}

	public String getGuardianAddress() {
		return guardianAddress;
	}

	public void setGuardianAddress(String guardianAddress) {
		this.guardianAddress = guardianAddress;
	}

}
